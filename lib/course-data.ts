export interface Exercise {
  type: 'translate' | 'choose' | 'fill' | 'match' | 'order'
  question: string
  options?: string[]
  answer: string | string[]
  hint?: string
  explanation?: string
}

export interface VocabWord {
  greek: string
  transliteration: string
  translation: string
  frequency?: number   // сколько раз встречается в НЗ
  example?: string     // пример из НЗ
}

export interface LessonSection {
  type: 'intro' | 'theory' | 'table' | 'example' | 'verse' | 'tip' | 'warning'
  title?: string
  content: string
  data?: Record<string, string>[]  // для таблиц
}

export interface Lesson {
  id: string
  moduleId: number
  title: string
  subtitle: string
  duration: string
  isFree: boolean
  sections: LessonSection[]
  vocab: VocabWord[]
  exercises: Exercise[]
  summary: string[]   // ключевые выводы урока
}

export interface Module {
  id: number
  title: string
  subtitle: string
  description: string
  lessons: Lesson[]
}

