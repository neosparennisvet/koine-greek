export function getAdjacentLessons(id: string) {
  const all = getAllLessons()
  const idx = all.findIndex(l => l.id === id)
  return {
    prev: idx > 0 ? all[idx - 1] : null,
    next: idx < all.length - 1 ? all[idx + 1] : null,
  }
}

