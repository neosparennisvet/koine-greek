export interface Exercise {
  type: 'translate' | 'choose' | 'fill' | 'match'
  question: string
  options?: string[]
  pairs?: Array<{ left: string; right: string }>  // для match
  answer: string | string[]
  hint?: string
  explanation?: string
}

export interface VocabWord {
  greek: string
  transliteration: string
  translation: string
  frequency?: number
  example?: string
}

export interface LessonSection {
  type: 'intro' | 'theory' | 'table' | 'example' | 'verse' | 'tip' | 'warning' | 'illustration'
  title?: string
  content: string
  data?: Record<string, string>[]
  illustration?: string  // SVG string или тип иллюстрации
}

export interface Lesson {
  id: string
  moduleId: number
  title: string
  subtitle: string
  duration: string
  isFree: boolean
  sections: LessonSection[]
  vocab: VocabWord[]
  exercises: Exercise[]
  summary: string[]
}

export interface Module {
  id: number
  title: string
  subtitle: string
  description: string
  lessons: Lesson[]
}

