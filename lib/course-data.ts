export function getAllLessons(): Lesson[] {
  return COURSE_MODULES.flatMap(m => m.lessons)
}

export function getLessonById(id: string): Lesson | undefined {
  return getAllLessons().find(l => l.id === id)
}

export function getAdjacentLessons(id: string): { prev: Lesson | null; next: Lesson | null } {
  const all = getAllLessons()
  const idx = all.findIndex(l => l.id === id)
  return {
    prev: idx > 0 ? all[idx - 1] : null,
    next: idx < all.length - 1 ? all[idx + 1] : null,
  }
}

export function getModuleByLessonId(id: string): Module | undefined {
  return COURSE_MODULES.find(m => m.lessons.some(l => l.id === id))
}
